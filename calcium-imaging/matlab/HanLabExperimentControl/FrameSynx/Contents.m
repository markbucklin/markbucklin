% The FrameSynx toolbox for MATLAB was built to synchronize continuous
% image acquisition with experiments conducted in the neuroscience
% laboratory setting. While the experiments are conducted in separate
% software (and potentially on a different computer), FrameSynx listens for
% messages to start/stop the experiment, start a trial, etc. and responds
% accordingly by controlling one or multiple cameras and illumination
% devices, and synchronizing this information with the data acquired.
%
